// Validates translation output chunks and merges into a generated cache file.
import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SCRATCH = "C:/Users/minja/AppData/Local/Temp/claude/d--Claude-Cowork/4f301435-7f73-45f7-9e59-702f13625be3/scratchpad";
const OUT_DIR = path.join(SCRATCH, "out");
const EXTRACT = path.join(__dirname, ".tmp-korean-strings.json");
const GENERATED = path.resolve(__dirname, "../apps/web/src/lib/mirilook-translations.generated.ts");

const KOREAN = /[가-힣ㄱ-ㅎㅏ-ㅣ]/;

const { strings: sourceStrings } = JSON.parse(await fs.readFile(EXTRACT, "utf8"));
const sourceSet = new Set(sourceStrings);

const dict = {}; // ko -> {zh, ja, en}
let total = 0;
const residual = [];
const missingFields = [];

const files = (await fs.readdir(OUT_DIR)).filter((f) => /^out-\d+\.json$/.test(f)).sort();
for (const f of files) {
  let arr;
  try {
    arr = JSON.parse(await fs.readFile(path.join(OUT_DIR, f), "utf8"));
  } catch (e) {
    console.error(`PARSE FAIL ${f}: ${e.message}`);
    continue;
  }
  for (const row of arr) {
    if (!row || typeof row.ko !== "string") continue;
    total++;
    if (!row.zh || !row.ja || !row.en) {
      missingFields.push(row.ko);
      continue;
    }
    // Residual Korean check (brand 미리룩 already mapped; flag others)
    for (const lang of ["zh", "ja", "en"]) {
      if (KOREAN.test(row[lang])) residual.push(`[${lang}] ${row.ko} => ${row[lang]}`);
    }
    dict[row.ko] = { zh: row.zh, ja: row.ja, en: row.en };
  }
}

// Coverage: which source strings are missing from dict?
const missing = sourceStrings.filter((s) => !(s in dict));
// Extra: dict keys not in source (harmless but report)
const extra = Object.keys(dict).filter((k) => !sourceSet.has(k));

console.log(`Source strings:        ${sourceStrings.length}`);
console.log(`Translated rows total: ${total}`);
console.log(`Unique dict entries:   ${Object.keys(dict).length}`);
console.log(`Missing coverage:      ${missing.length}`);
console.log(`Missing zh/ja/en field:${missingFields.length}`);
console.log(`Residual Korean:       ${residual.length}`);
console.log(`Extra (not in source): ${extra.length}`);

if (missing.length) {
  await fs.writeFile(path.join(SCRATCH, "missing.json"), JSON.stringify(missing, null, 2), "utf8");
  console.log("\nFirst 20 missing:");
  missing.slice(0, 20).forEach((s) => console.log("  " + JSON.stringify(s)));
}
if (residual.length) {
  console.log("\nFirst 20 residual Korean:");
  residual.slice(0, 20).forEach((s) => console.log("  " + s));
}

// Always (re)write generated file from current dict, but only finalize when clean unless --force.
const force = process.argv.includes("--force");
if (missing.length === 0 && residual.length === 0 && missingFields.length === 0 || force) {
  // Sort keys for stable diffs
  const sortedKeys = Object.keys(dict).sort((a, b) => a.localeCompare(b, "ko"));
  const entries = sortedKeys.map((ko) => {
    const v = dict[ko];
    return `  [${JSON.stringify(ko)}, { zh: ${JSON.stringify(v.zh)}, ja: ${JSON.stringify(v.ja)}, en: ${JSON.stringify(v.en)} }],`;
  });
  const header = `// AUTO-GENERATED by scripts/merge-translations.mjs — do not edit by hand.
// Korean (한국어) is the base/source language and is intentionally NOT included here.
// Regenerate: node scripts/extract-korean-strings.mjs && node scripts/merge-translations.mjs
// Entries: ${sortedKeys.length}

export type MirilookGeneratedLocale = "zh" | "ja" | "en";

export const mirilookGeneratedTranslations: Array<
  [string, Record<MirilookGeneratedLocale, string>]
> = [
${entries.join("\n")}
];
`;
  await fs.writeFile(GENERATED, header, "utf8");
  console.log(`\nWrote ${sortedKeys.length} entries -> ${GENERATED}`);
} else {
  console.log("\nNot writing generated file (issues above). Re-run with --force to write anyway.");
}
